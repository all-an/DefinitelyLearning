//aula operadores de comparação

package principal;

public class PrimeiroPrograma {
		public static void main(String[] args) {
			int n1, n2;
			
			n1 = 10;
			n2 = 20;
			
			System.out.println(n1 > n2);
			System.out.println(n1 < n2);
			System.out.println(n1 == n2);
			System.out.println(n1 <= n2);
			System.out.println(n1 != n2);
		}
}
